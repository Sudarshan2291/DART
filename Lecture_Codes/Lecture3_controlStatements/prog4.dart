// Switch

void main(){
  double x =6.5;
  switch(x){
    case 1.5:
      print("Mon");
    
    case 2.5:
      print("Tue");
    
    case 3.5:
      print("Wed");

    case 4.5:
      print("Thurs");

    case 5.5:
      print("Fri");

    case 6.5:
      print("Sat");

    case 7.5:
      print("Sun");
    
    default:
      print("Wrong Input");
  }
}